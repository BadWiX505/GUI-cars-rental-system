    public void UpdateTableCars() {
    	// this for bg of jTable
    	int necessaryRows=0;
        DefaultTableModel defaultTableModel = (DefaultTableModel) jTable2.getModel();
        defaultTableModel.setRowCount(0);
        List<Car> disponibleCars = Admin.consultDisponibleCars();
        if(disponibleCars.size()<4) necessaryRows=4-disponibleCars.size();
        for(Car car : disponibleCars) {
        	Object [] carRow = {car.getCarNumber(),car.getBrand(),car.getModel(),car.getYear()};
        	defaultTableModel.addRow(carRow);
        }
        for(int i=1;i<=necessaryRows;i++) {
        	Object [] carRow = {null,null,null,null};
        	defaultTableModel.addRow(carRow);
        }
    }
    
    
    public void UpdateTableCus() {
    	// this for bg of jTable
    	int necessaryRows=0;
        DefaultTableModel defaultTableModel = (DefaultTableModel) jTable4.getModel();
        defaultTableModel.setRowCount(0);
        List<Customer> Customers = Admin.ShowCus();
        if(Customers.size()<4) necessaryRows=4-Customers.size();
        for(Customer cus : Customers) {
        	Object [] carRow = {cus.getCin(),cus.getFirstname(),cus.getLastname(),cus.getTel()};
        	defaultTableModel.addRow(carRow);
        }
        for(int i=1;i<=necessaryRows;i++) {
        	Object [] carRow = {null,null,null,null};
        	defaultTableModel.addRow(carRow);
        }
    }


