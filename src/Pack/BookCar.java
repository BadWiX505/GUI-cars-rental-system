    private void kButton1ActionPerformed(java.awt.event.ActionEvent evt) {    
    	try {
			exceptions.CarsExceptions.principalExceptionsBook(this);
			if(selectedCarNum==null || selectedCin==null) {
				exceptions.CarsExceptions.showErrorMessage("Select necessary elements!");
			}
			else {
				if(Admin.bookCar(selectedCin, selectedCarNum,Tools.DateTools.ToDate(jTextField1.getText()),((Number)jSpinner1.getValue()).intValue())>0) {
					JOptionPane.showMessageDialog(null,"Booked successfully");
					UpdateTableCars();
					selectedCarNum=null;
					selectedCin=null;
					reset();
					this.repaint();
				}
			}
		} catch (CarsExceptions e) {
			e.printStackTrace();
			exceptions.CarsExceptions.showErrorMessage(e.getMessage());
		}
    }                                        

    
    
